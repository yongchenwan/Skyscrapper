class MaxArea {
    int max_area;
    int max_j;
    int max_i;
    int max_ilength;
    int max_jlength;
    public MaxArea(int max_area, int max_i, int max_ilength, int max_j, int max_jlength){
        this.max_area = max_area;
        this.max_i = max_i;
        this.max_ilength = max_ilength;
        this.max_j = max_j;
        this.max_jlength = max_jlength;
    }
}
